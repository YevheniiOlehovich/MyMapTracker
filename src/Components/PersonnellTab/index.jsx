import React, { useMemo, useState } from 'react';
import { useDispatch } from 'react-redux';
import {
    createColumnHelper,
    getCoreRowModel,
    getSortedRowModel,
    getFilteredRowModel,
    useReactTable,
    flexRender,
} from '@tanstack/react-table';
import { usePersonnelData, useDeletePersonnel } from '../../hooks/usePersonnelData';
import { useGroupsData } from '../../hooks/useGroupsData';
import { openAddPersonalModal } from '../../store/modalSlice';
import Styles from './styled';
import Button from '../Button';
import EditIco from '../../assets/ico/edit-icon-black.png';
import DelIco from '../../assets/ico/del-icon-black.png';
import { personalFunctions } from '../../helpres';

export default function PersonnelTab() {
    const dispatch = useDispatch();
    const { data: personnel = [], isLoading, isError, error } = usePersonnelData();
    const { data: groups = [] } = useGroupsData();
    const deletePersonnel = useDeletePersonnel();
    const [globalFilter, setGlobalFilter] = useState('');
    const [sorting, setSorting] = useState([]);

    const columnHelper = createColumnHelper();

    const functionMap = useMemo(() => {
        return personalFunctions.reduce((acc, curr) => {
        acc[curr._id] = curr.name;
        return acc;
        }, {});
    }, []);

    const groupMap = useMemo(() => {
        return groups.reduce((acc, group) => {
        acc[group._id] = group.name;
        return acc;
        }, {});
    }, [groups]);

    const handleAdd = () => {
        dispatch(openAddPersonalModal({}));
    };

    const handleEdit = (personId) => {
        dispatch(openAddPersonalModal({ personId }));
    };

    const handleDelete = (personId) => {
        if (window.confirm('–í–∏ –¥—ñ–π—Å–Ω–æ —Ö–æ—á–µ—Ç–µ –≤–∏–¥–∞–ª–∏—Ç–∏ —Ü—å–æ–≥–æ —Å–ø—ñ–≤—Ä–æ–±—ñ—Ç–Ω–∏–∫–∞?')) {
        deletePersonnel.mutate(personId, {
            onError: (err) => {
            console.error('–ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ –≤–∏–¥–∞–ª–µ–Ω–Ω—ñ:', err);
            alert('–ü–æ–º–∏–ª–∫–∞ –≤–∏–¥–∞–ª–µ–Ω–Ω—è');
            },
        });
        }
    };

    const columns = useMemo(() => [
        {
        id: 'rowNumber',
        header: '#',
        cell: info => info.row.index + 1,
        },
        columnHelper.accessor(row => groupMap[row.groupId] || '‚Äî', {
        id: 'groupName',
        header: '–ì—Ä—É–ø–∞',
        enableGlobalFilter: true,
        }),
        columnHelper.accessor(row => `${row.firstName} ${row.lastName}`, {
        id: 'fullName',
        header: '–ü–Ü–ë',
        enableGlobalFilter: true,
        }),
        columnHelper.accessor('rfid', {
        header: 'RFID',
        enableGlobalFilter: true,
        }),
        columnHelper.accessor('function', {
        header: '–ü–æ—Å–∞–¥–∞',
        enableGlobalFilter: false,
        cell: info => functionMap[info.getValue()] || info.getValue(),
        }),
        columnHelper.accessor('contactNumber', {
        header: '–¢–µ–ª–µ—Ñ–æ–Ω',
        enableGlobalFilter: false,
        }),
        columnHelper.accessor('note', {
        header: '–ü—Ä–∏–º—ñ—Ç–∫–∞',
        enableGlobalFilter: false,
        }),
        {
        id: 'actions',
        header: '–î—ñ—ó',
        cell: info => (
            <div style={{ display: 'flex', gap: 8 }}>
            <Styles.button onClick={() => handleEdit(info.row.original._id)} title="–†–µ–¥–∞–≥—É–≤–∞—Ç–∏">
                <Styles.ico $pic={EditIco} />
            </Styles.button>
            <Styles.button onClick={() => handleDelete(info.row.original._id)} title="–í–∏–¥–∞–ª–∏—Ç–∏">
                <Styles.ico $pic={DelIco} />
            </Styles.button>
            </div>
        ),
        },
    ], [handleEdit, handleDelete, functionMap, groupMap]);

    const table = useReactTable({
        data: personnel,
        columns,
        state: {
        globalFilter,
        sorting,
        },
        onGlobalFilterChange: setGlobalFilter,
        onSortingChange: setSorting,
        getCoreRowModel: getCoreRowModel(),
        getSortedRowModel: getSortedRowModel(),
        getFilteredRowModel: getFilteredRowModel(),
    });

    if (isLoading) return <div>–ó–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è...</div>;
    if (isError) return <div>–ü–æ–º–∏–ª–∫–∞: {error.message}</div>;

    return (
        <Styles.wrapper>
            <Styles.header>
                <Styles.searchInput
                type="text"
                placeholder="–ü–æ—à—É–∫ –ø–æ –≥—Ä—É–ø—ñ, –ü–Ü–ë, RFID..."
                value={globalFilter}
                onChange={e => setGlobalFilter(e.target.value)}
                />
                <Button text="–î–æ–¥–∞—Ç–∏" onClick={handleAdd} />
            </Styles.header>

            <Styles.tableContainer>
                <Styles.table>
                <thead>
                    {table.getHeaderGroups().map(headerGroup => (
                    <tr key={headerGroup.id}>
                        {headerGroup.headers.map(header => (
                        <th
                            key={header.id}
                            onClick={header.column.getToggleSortingHandler()}
                            style={{ cursor: 'pointer', userSelect: 'none' }}
                        >
                            {flexRender(header.column.columnDef.header, header.getContext())}
                            {header.column.getIsSorted() === 'asc' ? ' üîº' : header.column.getIsSorted() === 'desc' ? ' üîΩ' : ''}
                        </th>
                        ))}
                    </tr>
                    ))}
                </thead>
                <tbody>
                    {table.getRowModel().rows.map(row => (
                    <tr key={row.id}>
                        {row.getVisibleCells().map(cell => (
                        <td key={cell.id}>
                            {flexRender(cell.column.columnDef.cell, cell.getContext())}
                        </td>
                        ))}
                    </tr>
                    ))}
                </tbody>
                </Styles.table>
            </Styles.tableContainer>
        </Styles.wrapper>
    );
}
